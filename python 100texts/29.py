"""一个五位数，判断他是不是回文数，即12321是回文数，个位与万位相同，十位与千位相同"""
i = int(input("输入："))
j = int(i / 10000)
k = int((i - 10000 * j) / 1000)
x = int((i - 10000 * j - 1000 * k) / 100)
y = int((i - 10000 * j - 1000 * k - 100 * x) / 10)
z = int(i - 10000 * j - 1000 * k - 100 * x - 10 * y)
if z == j and k == y:
    print("yes")
else:
    print("NO")
